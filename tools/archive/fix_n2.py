# ── 1. Fix on/kun/meaning for N2 kanji ──
fixes_applied = 0
not_found = []

for kanji, (hanviet, on, kun, meaning_vi, word, reading, meaning_word) in n2_ref.items():
    m = re.search(r'kanji:"' + re.escape(kanji) + r'"[^\n]+', content)
    if not m:
        not_found.append(kanji)
        continue
    entry = m.group(0)
    new_entry = entry

    # Fix on
    on_m = re.search(r'on:"([^"]+)"', entry)
    if on_m and on_m.group(1) != on:
        new_entry = new_entry.replace(f'on:"{on_m.group(1)}"', f'on:"{on}"')
        fixes_applied += 1

    # Fix meaning
    meaning_m = re.search(r',meaning:"([^"]+)"', entry)
    if meaning_m and meaning_m.group(1) != meaning_vi:
        new_entry = new_entry.replace(f'meaning:"{meaning_m.group(1)}"', f'meaning:"{meaning_vi}"')
        fixes_applied += 1

    if new_entry != entry:
        content = content.replace(entry, new_entry, 1)

print(f'Field fixes: {fixes_applied} | Not found: {len(not_found)}')
if not_found:
    print('Not found:', not_found)

# ── 2. Fix w/r/m for N2 kanji ──
word_fixes = miss = 0
for kanji, (hanviet, on, kun, meaning_vi, word, reading, meaning_word) in n2_ref.items():
    pattern = re.compile(
        r'(kanji:"' + re.escape(kanji) + r'"[^\n]*words:\[)'
        r'\{"w": "[^"]*", "r": "[^"]*", "m": "[^"]*"\}'
    )
    new_word = f'{{"w": "{word}", "r": "{reading}", "m": "{meaning_word}"}}'
    new_content, n = pattern.subn(r'\g<1>' + new_word, content)
    if n:
        content = new_content
        word_fixes += 1
    else:
        miss += 1
        if kanji not in not_found:
            print(f'Word not matched: {kanji}')

with open('js/kanji-data.js', 'w', encoding='utf-8') as f:
    f.write(content)

print(f'Word fixes: {word_fixes} | ❌ Miss: {miss}')
