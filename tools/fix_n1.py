# ── 1. Fix on/meaning fields ──
field_fixes = 0
not_found = []

for kanji, (on, meaning_vi, word, reading, meaning_word) in n1_ref.items():
    lines = content.split('\n')
    matched = False
    for i, line in enumerate(lines):
        if f'kanji:"{kanji}"' in line and 'level:"N1"' in line:
            new_line = line
            # Fix on (only if non-empty)
            if on:
                on_m = re.search(r'on:"([^"]+)"', line)
                if on_m and on_m.group(1) != on:
                    new_line = new_line.replace(f'on:"{on_m.group(1)}"', f'on:"{on}"')
                    field_fixes += 1
            # Fix meaning
            meaning_m = re.search(r',meaning:"([^"]+)"', line)
            if meaning_m and meaning_m.group(1) != meaning_vi:
                new_line = new_line.replace(f'meaning:"{meaning_m.group(1)}"', f'meaning:"{meaning_vi}"')
                field_fixes += 1
            if new_line != line:
                lines[i] = new_line
                content = '\n'.join(lines)
            matched = True
            break
    if not matched:
        not_found.append(kanji)

print(f'Field fixes: {field_fixes} | Not in N1: {len(not_found)}')
if not_found:
    print(f'  Not found as N1 (likely lower level duplicate): {not_found[:20]}')

# ── 2. Fix w/r/m ──
word_fixes = word_miss = 0
for kanji, (on, meaning_vi, word, reading, meaning_word) in n1_ref.items():
    if kanji in not_found:
        continue
    pattern = re.compile(
        r'(kanji:"' + re.escape(kanji) + r'"[^\n]*level:"N1"[^\n]*words:\[)'
        r'\{"w": "[^"]*", "r": "[^"]*", "m": "[^"]*"\}'
    )
    new_word = f'{{"w": "{word}", "r": "{reading}", "m": "{meaning_word}"}}'
    new_content, n = pattern.subn(r'\g<1>' + new_word, content)
    if n:
        content = new_content
        word_fixes += 1
    else:
        word_miss += 1
        if word_miss <= 10:
            print(f'  Word not matched: {kanji}')

with open('js/kanji-data.js', 'w', encoding='utf-8') as f:
    f.write(content)

print(f'Word fixes: {word_fixes} | Miss: {word_miss}')
print('Done!')
